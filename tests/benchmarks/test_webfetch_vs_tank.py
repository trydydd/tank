# Benchmark
# ---------------------------------------------------------------------------


@pytest.mark.benchmark
def test_webfetch_vs_tank(bench_db: Database) -> None:
    # ------------------------------------------------------------------
    # 0. Natural-language query measurement
    #    Run the original NL query first to record whether Tank can answer
    #    it at all. A 0-result NL query is a real limitation and must be
    #    surfaced in the results JSON, not buried in a comment.
    # ------------------------------------------------------------------
    nl_result = _search_docs(
        bench_db,
        query=NATURAL_LANGUAGE_QUERY,
        packages=["fastmcp"],
        limit=10,
    )
    nl_hits = nl_result.get("results", [])
    nl_result_count = len(nl_hits)

    # ------------------------------------------------------------------
    # 1. WebFetch baseline — full page, no filtering.
    #    Strip HTML comment lines that are benchmark bookkeeping artifacts
    #    (not real web content) before counting tokens.
    # ------------------------------------------------------------------
    raw_page = FIXTURE_FILE.read_text(encoding="utf-8")
    page_content = _strip_comment_lines(raw_page)
    webfetch_chars = len(page_content)
    webfetch_tokens = _count_tokens(page_content)

    # ------------------------------------------------------------------
    # 2. Tank single-step full (agentless — summary scan + fetch all chunks)
    #    Represents the cost if an agent fetches every matched chunk without
    #    reading summaries to narrow down the selection first.
    # ------------------------------------------------------------------
    single_step_summary = _search_docs(
        bench_db,
        query=FTS5_QUERY,
        packages=["fastmcp"],
        limit=10,
    )
    single_step_hits = single_step_summary.get("results", [])
    assert single_step_hits, (
        f"FTS5 query {FTS5_QUERY!r} returned no results — benchmark is invalid. "
        "Update FTS5_QUERY to a term that exists in the fixture document."
    )
    single_step_ids = [r["chunk_id"] for r in single_step_hits]
    full_result = _fetch_docs(bench_db, single_step_ids)
    full_hits = full_result.get("results", [])
    full_tokens = sum(_count_tokens(r.get("content") or "") for r in full_hits)
    full_chunks_returned = len(full_hits)

    # ------------------------------------------------------------------
    # 3. Tank two-step (agentless — ALL matched chunks fetched, no agent
    #    selection after reading summaries)
    # ------------------------------------------------------------------
    summary_result = _search_docs(
        bench_db,
        query=FTS5_QUERY,
        packages=["fastmcp"],
        limit=20,
    )
    summary_hits = summary_result.get("results", [])
    assert summary_hits, (
        f"FTS5 query {FTS5_QUERY!r} returned no summary results — benchmark is invalid."
    )
    summary_tokens = sum(_count_tokens(r.get("summary") or "") for r in summary_hits)

    matched_ids = [r["chunk_id"] for r in summary_hits]
    fetch_result = _fetch_docs(bench_db, matched_ids)
    fetch_hits = fetch_result.get("results", [])
    fetch_tokens = sum(_count_tokens(r.get("content") or "") for r in fetch_hits)

    two_step_total = summary_tokens + fetch_tokens

    # ------------------------------------------------------------------
    # Per-chunk breakdown for two-step.
    # heading_path_unique: flag when all chunks share the same heading_path,
    # which indicates the chunker did not extract section headings and the
    # structural signal is absent.
    # ------------------------------------------------------------------
    chunk_breakdown = []
    for r in fetch_hits:
        content = r.get("content") or ""
        summary = r.get("summary") or ""
        chunk_breakdown.append(
            {
                "chunk_id": r["chunk_id"],
                "heading_path": r.get("heading_path"),
                "summary": summary,
                "content_chars": len(content),
                "content_tokens": _count_tokens(content),
            }
        )

    heading_paths = [c["heading_path"] for c in chunk_breakdown]
    heading_paths_distinct = len(set(heading_paths))
    heading_path_warning = heading_paths_distinct == 1 and len(chunk_breakdown) > 1

    # ------------------------------------------------------------------
    # Results payload
    # ------------------------------------------------------------------
    results_payload: dict[str, Any] = {
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "git_commit": _git_commit(),
        "tank_version": tank.__version__,
        "token_counter": "len_div_4",
        "source_url": SOURCE_URL,
        "natural_language_query": NATURAL_LANGUAGE_QUERY,
        "nl_query_result_count": nl_result_count,
        "nl_query_note": (
            "The natural language query returned 0 FTS5 results — the terms "
            "'configure' and 'implementation' do not appear in the source document. "
            "The FTS5_QUERY was generated by an LLM from this NL query before the "
            "document was read; those terms do match and drive the comparison."
        )
        if nl_result_count == 0
        else (f"The natural language query returned {nl_result_count} result(s)."),
        "fts5_query": FTS5_QUERY,
        "webfetch": {
            "chars": webfetch_chars,
            "tokens": webfetch_tokens,
            "note": "HTML comment lines stripped (benchmark bookkeeping, not real page content).",
        },
        "tank_single_step_full": {
            "chunks_returned": full_chunks_returned,
            "tokens": full_tokens,
            "pct_of_webfetch": round(100 * full_tokens / webfetch_tokens)
            if webfetch_tokens
            else 0,
            "tokens_saved": webfetch_tokens - full_tokens,
            "pct_saved": round(100 * (webfetch_tokens - full_tokens) / webfetch_tokens)
            if webfetch_tokens
            else 0,
        },
        "tank_two_step_agentless": {
            "step1_summary_tokens": summary_tokens,
            "step2_full_tokens": fetch_tokens,
            "total_tokens": two_step_total,
            "pct_of_webfetch": round(100 * two_step_total / webfetch_tokens)
            if webfetch_tokens
            else 0,
            "tokens_saved": webfetch_tokens - two_step_total,
            "pct_saved": round(
                100 * (webfetch_tokens - two_step_total) / webfetch_tokens
            )
            if webfetch_tokens
            else 0,
            "chunk_breakdown": chunk_breakdown,
        },
        "warnings": [],
        "notes": (
            "Two-step approach is AGENTLESS: all matched chunk IDs are fetched "
            "unconditionally. A real agent selecting only relevant chunks after "
            "reading summaries would reduce token cost further."
        ),
    }

    if heading_path_warning:
        results_payload["warnings"].append(
            "All returned chunks share the same heading_path "
            f"({heading_paths[0]!r}). The chunker did not extract section "
            "headings from this document — structural signal is absent."
        )

    if nl_result_count == 0:
        results_payload["warnings"].append(
            "Natural language query returned 0 FTS5 results. The LLM-generated "
            "FTS5 query is what an agent would actually send — this correctly "
            "demonstrates the NL→FTS5 translation step working as intended."
        )

    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    out_path = RESULTS_DIR / "webfetch_vs_tank_latest.json"
    out_path.write_text(json.dumps(results_payload, indent=2))

    # ------------------------------------------------------------------
    # Console output
    # ------------------------------------------------------------------
    print("\n── WebFetch vs Tank Token Comparison ──────────────────────────")
    print(f"  git commit   : {results_payload['git_commit']}")
    print(f"  tank version : {results_payload['tank_version']}")
    print(f"  source       : {SOURCE_URL}")
    print(f"  query (NL)   : {NATURAL_LANGUAGE_QUERY}")
    print(f"  query (FTS5) : {FTS5_QUERY}")
    print()
    if nl_result_count == 0:
        print(
            "  ℹ️  NL query returned 0 FTS5 results. LLM-generated FTS5 query does match."
        )
    else:
        print(f"  NL query returned {nl_result_count} result(s).")
    print()
    print(f"  {'Approach':<40} {'tokens':>7}  {'vs WebFetch':>11}  {'saved':>7}")
    print(f"  {'-' * 40} {'-' * 7}  {'-' * 11}  {'-' * 7}")
    print(
        f"  {'WebFetch (full page, comments stripped)':<40} {webfetch_tokens:>7}  {'100%':>11}  {'—':>7}"
    )
    pct = results_payload["tank_single_step_full"]["pct_of_webfetch"]
    saved = results_payload["tank_single_step_full"]["pct_saved"]
    print(
        f"  {'Tank single-step full (agentless)':<40} {full_tokens:>7}"
        f"  {pct:>10}%  {saved:>6}%"
    )
    pct2 = results_payload["tank_two_step_agentless"]["pct_of_webfetch"]
    saved2 = results_payload["tank_two_step_agentless"]["pct_saved"]
    print(
        f"  {'Tank two-step (agentless)':<40} {two_step_total:>7}"
        f"  {pct2:>10}%  {saved2:>6}%"
    )
    print(f"    ↳ step 1 summary scan                {summary_tokens:>7}")
    print(
        f"    ↳ step 2 full fetch ({len(fetch_hits)} chunks)        {fetch_tokens:>7}"
    )
    print()
    print("  Per-chunk breakdown (step 2):")
    print(f"    {'chunk':>5}  {'tokens':>7}  heading_path | summary")
    for c in chunk_breakdown:
        summary_short = (c["summary"] or "")[:50]
        hp = c["heading_path"] or ""
        print(
            f"    {c['chunk_id']:>5}  {c['content_tokens']:>7}  {hp} | {summary_short}"
        )
    print()
    if heading_path_warning:
        print(
            "  ⚠️  All chunks share the same heading_path — section headings not extracted."
        )
    print()
    print("  ⚠️  Two-step is AGENTLESS: all matched chunks fetched unconditionally.")
    print("     A real agent selecting only relevant chunks after reading summaries")
    print("     would reduce token cost further. That path is not yet benchmarked.")

    if results_payload["warnings"]:
        print()
        print("  Warnings:")
        for w in results_payload["warnings"]:
            print(f"    - {w}")

    print(f"\n  Results written to {out_path}")
    print("────────────────────────────────────────────────────────────────\n")
