"""WebFetch vs Tank token comparison benchmark.

Simulates two documentation retrieval strategies for the same query:

  1. WebFetch   — the full source page lands in context as-is
  2. Tank full  — fetch all matched chunks without a prior summary step (agentless)
  3. Tank 2-step — search (summaries) first, then fetch for ALL matched chunks

⚠️  The two-step approach here is AGENTLESS: all matched chunk IDs are
    fetched unconditionally after the summary scan. A real agent would read
    the summaries and fetch only the relevant chunks, reducing token cost
    further. That selective path is not yet benchmarked.

Source fixture: tests/benchmarks/fixtures/fastmcp-running-server.md
  Original URL: https://gofastmcp.com/deployment/running-server
  Fetched:      2026-05-21

Natural language query: "how do I configure a stdio implementation in fastmcp"

FTS5 query: "stdio transport run"
  Generated by an LLM from the natural language query above, before the
  document was read. This reflects realistic agent usage: an agent converts
  a user question to FTS5 keyword terms and queries Tank without knowing
  the document's vocabulary in advance.

  The NL query itself returns 0 FTS5 results ("configure" and
  "implementation" do not appear in the source). The LLM-derived FTS5 terms
  do match, which is the expected benefit of the NL→FTS5 translation step.
  Both measurements are recorded in the results JSON.

WebFetch baseline: the raw fixture file content, with comment-metadata lines
  (lines beginning with <!--) stripped. Those comment lines are benchmark
  bookkeeping artifacts, not content that would appear in a real web fetch.
  Stripping them ensures the WebFetch token count reflects real page content.

Run:
    pytest tests/benchmarks/test_webfetch_vs_tank.py --benchmark -v -s

Results are written to tests/benchmarks/results/webfetch_vs_tank_latest.json.
"""

from __future__ import annotations

import json
import shutil
import subprocess
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import pytest

import synd as tank
from synd.builder.build import build_pack
from synd.storage.db import Database
from synd.storage.models import Pack
from synd.server import fetch_docs as _fetch_docs
from synd.server import search_docs as _search_docs

FIXTURE_DIR = Path(__file__).parent / "fixtures"
RESULTS_DIR = Path(__file__).parent / "results"

FIXTURE_FILE = FIXTURE_DIR / "fastmcp-running-server.md"
SOURCE_URL = "https://gofastmcp.com/deployment/running-server"

NATURAL_LANGUAGE_QUERY = "how do I configure a stdio implementation in fastmcp"
FTS5_QUERY = "stdio transport run"


def _count_tokens(text: str) -> int:
    return len(text) // 4


def _git_commit() -> str:
    try:
        return subprocess.check_output(
            ["git", "rev-parse", "--short", "HEAD"], text=True
        ).strip()
    except Exception:
        return "unknown"


def _strip_comment_lines(text: str) -> str:
    """Remove HTML comment lines (<!-- ... -->) from page text.

    These are benchmark bookkeeping annotations added when the fixture was
    saved to disk. A real web fetch would never include them, so they must
    be excluded from the WebFetch token count to keep the comparison fair.
    """
    lines = [ln for ln in text.splitlines() if not ln.strip().startswith("<!--")]
    return "\n".join(lines)


